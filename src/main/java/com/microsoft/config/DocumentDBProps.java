package com.microsoft.config;

public final class DocumentDBProps {

	private String documentdb_host;
	private String documentdb_key;
	private String documentdb_database;
	private String documentdb_resource_id;

	public String getDocumentdb_host() {
		return documentdb_host;
	}

	public void setDocumentdb_host(String documentdb_host) {
		this.documentdb_host = documentdb_host;
	}

	public String getDocumentdb_key() {
		return documentdb_key;
	}

	public void setDocumentdb_key(String documentdb_key) {
		this.documentdb_key = documentdb_key;
	}

	public String getDocumentdb_database() {
		return documentdb_database;
	}

	public void setDocumentdb_database(String documentdb_database) {
		this.documentdb_database = documentdb_database;
	}

	public String getDocumentdb_resource_id() {
		return documentdb_resource_id;
	}

	public void setDocumentdb_resource_id(String documentdb_resource_id) {
		this.documentdb_resource_id = documentdb_resource_id;
	}
}
